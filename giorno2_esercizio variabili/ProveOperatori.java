public class ProveOperatori {


        public static void main(String[] args) {
            int a = 10;    // Operatore di assegnazione semplice (=)
            System.out.println("Valore iniziale di a: " + a);

            a += 5;        // Equivale a: a = a + 5;
            System.out.println("Dopo a += 5: " + a);

            a -= 3;        // Equivale a: a = a - 3;
            System.out.println("Dopo a -= 3: " + a);

            a *= 2;        // Equivale a: a = a * 2;
            System.out.println("Dopo a *= 2: " + a);

            a /= 4;        // Equivale a: a = a / 4;
            System.out.println("Dopo a /= 4: " + a);

            a %= 3;        // Equivale a: a = a % 3;
            System.out.println("Dopo a %= 3: " + a);



            int x = 10;
            int y = 20;


            // Operatori di confronto restituiscono un valore booleano (true o false)
            System.out.println("x == y: " + (x == y));
            System.out.println("x != y: " + (x != y));
            System.out.println("x > y: " + (x > y));
            System.out.println("x < y: " + (x < y));
            System.out.println("x >= y: " + (x >= y));
            System.out.println("x <= y: " + (x <= y));



            boolean d = true;
            boolean e = false;

                    // Operatore AND logico: restituisce true solo se entrambi sono true
            System.out.println("a && b: " + (d && e));

                    // Operatore OR logico: restituisce true se almeno uno Ã¨ true
            System.out.println("a || b: " + (d || e));

                    // Operatore NOT logico: inverte il valore booleano
            System.out.println("!a: " + (!d));
            System.out.println("!b: " + (!e));


                }
            }







